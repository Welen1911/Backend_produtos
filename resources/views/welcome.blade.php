<script>
    $(window).on('hashchange', () => {
        if (window.location.hash) {
            let page = window.location.hash.replace('#', '');
            if (!page == Number.NaN || page <= 0) {
                getData(page);
            }
        }
    })

    function getData(page) {
        $.ajax({
            url: '?page='+page,
            type: "get",
            datatype: "html"
        })
    }
